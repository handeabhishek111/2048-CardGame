const web3 = new Moralis.Web3();
function checkStatus() {
  const user = Moralis.User.current();
  console.log("user: ", user.get("ethAddress"));
  const settings = {
    async: false,
    crossDomain: true,
    url: `/result/check/${user.get("ethAddress")}`,
    method: "get",
    processData: false,
  };

  $.ajax(settings).done(async function (response) {
    console.log(response);
    if (response && response.status) {
      $("#duel-start-btn").hide();
      $("#result-btn").css("display", "block");
      if (response.status == "loser") {
        alert("Sorry you have lost the Duel");
      } else if (response.status == "winner") {
        alert(
          "Congrats you have won the Duel, to claim the rewards you will have to burn your NFT"
        );
        await postWonChanges(user, response.tokenId);
      }
    } else if (response == false) {
      alert("No duel found");
    }
  });
}
